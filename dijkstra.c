int minDistance(int dist[], bool sptSet[]) {
    int min = INT_MAX, min_index;

    for (int v = 0; v < V; v++)
        if (sptSet[v] == false && dist[v] <= min)
            min = dist[v], min_index = v;

    return min_index;
}

void dijkstra(int graph[V][V], int src, int dest[]) {
    int dist[V];
    bool sptSet[V];

    for (int i = 0; i < V; i++) {
        dist[i] = INT_MAX;
        sptSet[i] = false;
    }

    dist[src] = 0;

    for (int count = 0; count < V - 1; count++) {
        int u = minDistance(dist, sptSet);
        sptSet[u] = true;

        for (int v = 0; v < V; v++) {
            if (!sptSet[v] && graph[u][v] && dist[u] != INT_MAX && dist[u] + graph[u][v] < dist[v]) {
                dist[v] = dist[u] + graph[u][v];
            }
        }
    }

    for (int i = 0; i < TOP; i++) {
        int min = INT_MAX, min_index = -1;
        for (int j = 35; j < V; j++) {
            if (dist[j] < min) {
                bool found = false;
                for (int k = 0; k < i; k++) {
                    if (j == dest[k]) {
                        found = true;
                        break;
                    }
                }
                if (!found) {
                    min = dist[j];
                    min_index = j;
                }
            }
        }
        if (min_index != -1) {
            dest[i] = min_index;
        }
    }

    printf("The top 5 nearest restaurants to %s are:\n", placeNames[src]);
    for (int i = 0; i < TOP; i++) {
        if (dest[i] != -1) {
            printf("%d. Nearest restaurant: %s\n", i + 1, restaurantNames[dest[i] - 35]);
        }
    }
}

