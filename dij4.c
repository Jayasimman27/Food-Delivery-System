// Function to find index of a place
int findPlaceIndex(const char* place) {
    for (int i = 0; i < sizeof(placeNames1)/sizeof(placeNames1[0]); ++i) {
        if (strcmp(place, placeNames1[i]) == 0) {
            return i;
        }
    }
    return -1; // Not found
}

// Function to find index of a restaurant
